// Dynamic catalog items storage
const dynamicCatalogItems: ICatalogItem[] = [];

export const addDynamicCatalogItem = (item: ICatalogItem): void => {
	dynamicCatalogItems.push(item);
};

export const getCatalogItemsResponseMock = (
	type?: EnumCatalogType
): ICatalogResponse => {
	let items: ICatalogItem[] = [];

	if (type === EnumCatalogType.Service) {
		items = [
			...catalogServicesMock,
			...dynamicCatalogItems.filter(
				(item) => item.type === EnumCatalogType.Service
			),
		];
	} else if (type === EnumCatalogType.Product) {
		items = [
			...catalogProductsMock,
			...dynamicCatalogItems.filter(
				(item) => item.type === EnumCatalogType.Product
			),
		];
	} else {
		items = [
			...catalogServicesMock,
			...catalogProductsMock,
			...dynamicCatalogItems,
		];
	}

	return {
		items,
		pagination: {
			page: 1,
			limit: 10,
			total: items.length,
			totalPages: Math.ceil(items.length / 10),
		},
		filters: {
			type,
		},
	};
};
